SELECT
    CC.WEIGHT AS [WEIGHT],
    P.SKU_PKT_HIGH_VALUE AS [UL],
    P.SKU_PKT_LOW_VALUE AS [LL],
    CASE 
        WHEN CAST(CC.DateAndTime AS TIME) BETWEEN '06:00:00' AND '13:59:59' THEN 'Shift A'
        WHEN CAST(CC.DateAndTime AS TIME) BETWEEN '14:00:00' AND '21:59:59' THEN 'Shift B'
        ELSE 'Shift C' 
    END AS [SHIFT]
FROM dbo.CW_DATA CC
CROSS APPLY [ITC_CheckWeigher_2.0].DBO.[UDTVT_CW_PKT_PARAMETER](CC.DateAndTime, CC.CW_NO) P
WHERE CC.DateAndTime BETWEEN '2022-06-10 00:00:00' AND '2022-06-11 00:00:00';